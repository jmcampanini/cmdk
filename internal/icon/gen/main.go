// Generator that downloads Nerd Fonts glyphnames.json and emits
// entries_generated.go containing only the cod, dev, and oct icon sets.
//
// Usage: go run ./internal/icon/gen
package main

import (
	"encoding/json"
	"fmt"
	"go/format"
	"io"
	"net/http"
	"os"
	"path/filepath"
	"runtime"
	"slices"
	"strconv"
	"strings"
	"time"
	"unicode"
	"unicode/utf16"
)

const (
	glyphURL = "https://raw.githubusercontent.com/ryanoasis/nerd-fonts/v3.4.0/glyphnames.json"
	outFile  = "entries_generated.go"
)

var allowedPrefixes = []string{"cod-", "dev-", "oct-"}

type glyphEntry struct {
	Code string `json:"code"`
}

type entry struct {
	Alias       string
	Code        rune
	Description string
}

func main() {
	if err := run(); err != nil {
		fmt.Fprintf(os.Stderr, "gen: %v\n", err)
		os.Exit(1)
	}
}

func run() error {
	data, err := download(glyphURL)
	if err != nil {
		return fmt.Errorf("download: %w", err)
	}

	entries, err := buildEntries(data)
	if err != nil {
		return err
	}

	formatted, err := render(entries)
	if err != nil {
		return err
	}

	pkgDir, err := packageDir()
	if err != nil {
		return err
	}
	outPath := filepath.Join(pkgDir, outFile)
	if err := os.WriteFile(outPath, formatted, 0o644); err != nil {
		return fmt.Errorf("write: %w", err)
	}

	fmt.Printf("wrote %d entries to %s\n", len(entries), outPath)
	return nil
}

func buildEntries(data []byte) ([]entry, error) {
	var raw map[string]json.RawMessage
	if err := json.Unmarshal(data, &raw); err != nil {
		return nil, fmt.Errorf("parse json: %w", err)
	}

	var entries []entry
	for key, val := range raw {
		if key == "METADATA" || !hasAllowedPrefix(key) {
			continue
		}

		var g glyphEntry
		if err := json.Unmarshal(val, &g); err != nil {
			return nil, fmt.Errorf("parse entry %q: %w", key, err)
		}

		code, err := parseGlyphCode(key, g.Code)
		if err != nil {
			return nil, err
		}

		entries = append(entries, entry{
			Alias:       "nf-" + key,
			Code:        code,
			Description: descriptionFromAlias(key),
		})
	}

	slices.SortFunc(entries, func(a, b entry) int {
		return strings.Compare(a.Alias, b.Alias)
	})
	return entries, nil
}

// parseGlyphCode accepts only Unicode scalar values: anything above
// unicode.MaxRune or in the surrogate range would encode as U+FFFD
// replacement characters in the generated source.
func parseGlyphCode(key, code string) (rune, error) {
	v, err := strconv.ParseUint(code, 16, 32)
	if err != nil {
		return 0, fmt.Errorf("parse code for %q (%s): %w", key, code, err)
	}
	if v > unicode.MaxRune {
		return 0, fmt.Errorf("code for %q (%s): exceeds max Unicode code point U+10FFFF", key, code)
	}
	r := rune(v)
	if utf16.IsSurrogate(r) {
		return 0, fmt.Errorf("code for %q (%s): surrogate code point is not a Unicode scalar value", key, code)
	}
	return r, nil
}

func render(entries []entry) ([]byte, error) {
	formatted, err := format.Source(generate(entries))
	if err != nil {
		return nil, fmt.Errorf("gofmt: %w", err)
	}
	return formatted, nil
}

func download(url string) ([]byte, error) {
	client := &http.Client{Timeout: 30 * time.Second}
	resp, err := client.Get(url)
	if err != nil {
		return nil, err
	}
	defer func() { _ = resp.Body.Close() }()

	if resp.StatusCode != http.StatusOK {
		body, _ := io.ReadAll(io.LimitReader(resp.Body, 256))
		return nil, fmt.Errorf("HTTP %d: %s", resp.StatusCode, strings.TrimSpace(string(body)))
	}
	return io.ReadAll(io.LimitReader(resp.Body, 10<<20))
}

func hasAllowedPrefix(key string) bool {
	return slices.ContainsFunc(allowedPrefixes, func(p string) bool {
		return strings.HasPrefix(key, p)
	})
}

// "cod-terminal_tmux" -> "Terminal tmux"
func descriptionFromAlias(key string) string {
	_, name, _ := strings.Cut(key, "-")
	name = strings.ReplaceAll(name, "_", " ")
	if len(name) == 0 {
		return ""
	}
	runes := []rune(name)
	runes[0] = unicode.ToUpper(runes[0])
	return string(runes)
}

func generate(entries []entry) []byte {
	var b strings.Builder
	b.WriteString("// Code generated by go run ./internal/icon/gen; DO NOT EDIT.\n\n")
	b.WriteString("package icon\n\n")
	b.WriteString("var entries = []Entry{\n")

	for _, e := range entries {
		fmt.Fprintf(&b, "\t{%q, %q, %q},\n", e.Alias, string(e.Code), e.Description)
	}

	b.WriteString("}\n")
	return []byte(b.String())
}

func packageDir() (string, error) {
	_, file, _, ok := runtime.Caller(0)
	if !ok {
		return "", fmt.Errorf("runtime.Caller failed; cannot determine package directory")
	}
	return filepath.Dir(filepath.Dir(file)), nil
}
